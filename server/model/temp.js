import mongoose from 'mongoose'

const temp = mongoose.model('temp', {
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    otp: {
        type: String,
        required: true
    }
});

export default temp;